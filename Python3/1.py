# This is a simple Python script to demonstrate variables, comments, and print statements.

# Variables
# Variables are used to store data. In Python, you can assign a value to a variable using the '=' operator.
# Variable names are case-sensitive and can contain letters, numbers, and underscores. They must start with a letter or underscore.

# Example variables
name = "John"  # A variable storing a string (text)
age = 25       # A variable storing an integer (whole number)
height = 1.75  # A variable storing a float (decimal number)
is_student = True  # A variable storing a boolean (True or False)

# Comments
# Comments are used to add explanations within the code. They are ignored by the Python interpreter.

# Printing variable values
# The print() function is used to display the values of variables.

print("Hello, " + name)  # Concatenating strings using the '+' operator
print("Age:", age)
print("Height:", height)

# Operations with variables
# You can perform operations with variables, such as mathematical calculations.

double_age = age * 2
height_in_meters = height * 100  # Converting height to centimeters

print("Double age:", double_age)
print("Height in centimeters:", height_in_meters)

# Using comments to explain code
# It's good practice to add comments to explain the purpose of your code, especially for beginners.

# Calculating the average
# The average is calculated by adding age and height and then dividing by 2.
average = (age + height) / 2

print("Average:", average)

