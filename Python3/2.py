# This is a simple Python script to explain while loops with a practical example.

# Example: Counting up to 5 using a while loop

# Initialize a variable
counter = 1

# While loop condition
# The loop will continue as long as the condition (counter <= 5) is true.
while counter <= 5:
    # Print the current value of the variable
    print(counter)
    
    # Increment the variable by 1 in each iteration
    counter += 1

# Print a message outside the loop
print("Counting complete!")

# Comments to explain the example:
# - Initialized a variable 'counter' with the value 1.
# - Used a while loop with the condition 'counter <= 5'.
# - Printed the current value of 'counter' in each iteration.
# - Incremented 'counter' by 1 in each iteration.
# - The loop continues until 'counter' is no longer less than or equal to 5.
# - Printed a message outside the loop after counting is complete.




