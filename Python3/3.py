# This is a simple Python script to explain for loops with a practical example.

# Example: Counting up to 5 using a for loop

# Use the range function to create a sequence of numbers from 1 to 5
# The loop will iterate over each value in the sequence.
for counter in range(1, 6):
    # Print the current value of the variable
    print(counter)

# Print a message outside the loop
print("Counting complete!")

# Comments to explain the example:
# - Used a for loop with the range function to iterate over a sequence of numbers (1 to 5).
# - Printed the current value of the loop variable 'counter' in each iteration.
# - The loop iterates over each value in the sequence.

