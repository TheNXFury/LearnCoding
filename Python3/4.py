# This is a simple Python script to explain if/else statements with a practical example.

# Example: Check if a number is positive or negative

# Get user input for a number
user_number = float(input("Enter a number: "))

# Use an if/else statement to check if the number is positive or negative
if user_number > 0:
    # If the number is greater than 0, it's positive
    print("The number is positive.")
elif user_number < 0:
    # If the number is less than 0, it's negative
    print("The number is negative.")
else:
    # If the number is neither greater nor less than 0, it must be 0
    print("The number is zero.")

# Comments to explain the example:
# - Used the input function to get a number from the user.
# - Used an if/else statement to check conditions:
#   - If the number is greater than 0, print "The number is positive."
#   - If the number is less than 0, print "The number is negative."
#   - If the number is neither greater nor less than 0, print "The number is zero."

